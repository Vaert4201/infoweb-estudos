dia = input('Digite qualquer número:')
if dia == 1:
    print('Segunda.')
elif dia == 2:
    print('Terça.')
elif dia == 3:
    print('Quarta.')
elif dia == 4:
    print('Quinta.')
elif dia == 5:
    print('Sexta.')
elif dia == 6:
    print('Sábado.')
elif dia == 7:
    print('Domingo.')
else:
    print('Dia inválido.')